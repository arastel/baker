function expand_dps()
{
    $('.plus_dps').removeClass('hidden');
    $('.plus_nautique').addClass('hidden');
    $('.plus_ps').addClass('hidden');
}

function expand_nautique()
{
    $('.plus_dps').addClass('hidden');
    $('.plus_nautique').removeClass('hidden');
    $('.plus_ps').addClass('hidden');
}

function expand_ps()
{
    $('.plus_dps').addClass('hidden');
    $('.plus_nautique').addClass('hidden');
    $('.plus_ps').removeClass('hidden');
}

function hide_plus()
{
    $('.plus_dps').addClass('hidden');
    $('.plus_nautique').addClass('hidden');
    $('.plus_ps').addClass('hidden');
}
